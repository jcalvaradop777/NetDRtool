    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:
        Integer ep = Integer.parseInt(snpDim.getValue().toString());
        dri.epocas = ep;
        
        Float lr = Float.parseFloat(snpDim.getValue().toString());
        dri.learningRate = lr;
        
        Integer dr = Integer.parseInt(snpDim.getValue().toString());
        dri.d = dr;
        ChooserEscritorio.setStatus("Dimension in NetDR is " + dr);
        dri.setInfo("Support in " + dr + "%");
        dri.getMnuRun().setEnabled(true);
        this.dispose();
}//GEN-LAST:event_btnAcceptActionPerformed

    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        ExampleFileFilter ext = new ExampleFileFilter("netdr", "Filtered Data");
        String path;
        String fileName;

        saveModel.addChoosableFileFilter(ext);
        int saveOK = saveModel.showSaveDialog(this);
        if(saveOK == saveModel.APPROVE_OPTION) {
            path = saveModel.getSelectedFile().getAbsolutePath();
            //fileName = saveModel.getName();
            dri.saveName = path+".netdr";
            System.out.println("Ruta: " + dri.saveName);
        }
    }//GEN-LAST:event_saveActionPerformed

