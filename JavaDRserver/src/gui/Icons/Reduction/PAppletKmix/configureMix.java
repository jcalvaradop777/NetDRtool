    public void buildPapplet3K(ArrayList nameKernels){
        
        mix3 = new PAppletKmix3();
        pnlPApplet.add(mix3);
        mix3.init();
        
        String nk1 = nameKernels.get(0).toString();
        if(nk1.length()>12) nk1 = nk1.substring(0,12);
        
        String nk2 = nameKernels.get(1).toString();
        if(nk2.length()>12) nk2 = nk2.substring(0,12);

        String nk3 = nameKernels.get(2).toString();
        if(nk3.length()>12)nk3 = nk3.substring(0,12);

        mix3.setTitlesKernels("K1:"+nk1, "K2:"+nk2, "K3:"+nk3);
        
        cualPApplet = 3;
    }
    
    public void buildPapplet2K(ArrayList nameKernels){
        
        mix2 = new PAppletKmix2();
        pnlPApplet.add(mix2);
        mix2.init();
        
        String nk1 = nameKernels.get(0).toString();
        if(nk1.length()>20) nk1 = nk1.substring(0,20);
        
        String nk2 = nameKernels.get(1).toString();
        if(nk2.length()>20) nk2 = nk2.substring(0,20);

        mix2.setTitlesKernels("K1:"+nk1, "K2:"+nk2);
        
        cualPApplet = 2;
    }
    
