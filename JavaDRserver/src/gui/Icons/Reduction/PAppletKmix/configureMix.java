    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        
        Integer dr = Integer.parseInt(snpDim.getValue().toString());
        
        if(metMix==1){
            
            kmixicon.d = dr;
        
            if(cualPApplet==2){
                kmixicon.R = mix2.R;
                kmixicon.B = mix2.B;
            }else if(cualPApplet==3){
                kmixicon.R = mix3.R;
                kmixicon.G = mix3.G;
                kmixicon.B = mix3.B;
            }
            kmixicon.getMnuRun().setEnabled(true);
            
        }else if(metMix==2){
            
            ddricon.d = dr;
        
            if(cualPApplet==2){
                ddricon.R = mix2.R;
                ddricon.B = mix2.B;
            }else if(cualPApplet==3){
                ddricon.R = mix3.R;
                ddricon.G = mix3.G;
                ddricon.B = mix3.B;
            }
            ddricon.getMnuRun().setEnabled(true);
        }
        
        
        btnexit.setEnabled(true);
    }//GEN-LAST:event_btnAcceptActionPerformed

