    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:
        float muc = sliderLocal.getValue();
        hoi.mu = muc/10;

        ChooserEscritorio.setStatus("Regularization factor defined for " + hoi.algorithm + " is " + muc + "%");
        hoi.setInfo("Regularization factor " + muc/10 + "%");
        hoi.getMnuRun().setEnabled(true);
        this.dispose();
}//GEN-LAST:event_btnAcceptActionPerformed

    private void sliderLocalStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_sliderLocalStateChanged
        float muc = sliderLocal.getValue();
        sliderGlobal.setValue((int)(10-muc));
//        hoi.mu = muc/10;
        valLocal.setText(String.valueOf(muc/10));
        valGlobal.setText(String.valueOf(formato.format(1-(muc/10))));
    }//GEN-LAST:event_sliderLocalStateChanged

